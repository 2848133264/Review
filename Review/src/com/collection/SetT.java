package com.collection;

public class SetT {

	/**
	 * set 接口，它的实现类有这个hashset，hashtable,treeset
	 * 
	 * set集合：最大的特点是，无序的，不能重复相同的元素。
	 * set 中可以放入空值，同理也是只能放入一个null的元素
	 * 
	 */
	
	/**
	 * Hashset
	 * Hashset 对每个对象采用哈希散列码进行存储索引，哈希散列码有
	 * 程序在添加时才生成，每个对象之间没有关系
	 * 这样在hashset 在进添加、删除对象或者比较对象，以及随机读取一个
	 * 对象操作时运算的时间是一直的，不会因为对象数据量的多少的变化影响性能。
	 */
	
	/**
	 * treeset
	 * treeset:的数据结构可以理解为二叉树结构，我们可以使用Treeset 实现排序功能，treeset通过定义一个比较器comparator来完成对象的排序
	 * 
	 */
	
	
}
